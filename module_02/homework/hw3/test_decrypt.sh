#!/bin/bash

# Массив входных данных
inputs=(
    "абра-кадабра."
    "абраа..-кадабра"
    "абраа..-.кадабра"
    "абра--..кадабра"
    "абрау...-кадабра"
    "абра........"
    "абр......a."
    "1..2.3"
    "."
    "1......................."
)

# Массив ожидаемых ответов
expected=(
    "абра-кадабра"
    "абра-кадабра"
    "абра-кадабра"
    "абра-кадабра"
    "абра-кадабра"
    ""
    "a"
    "23"
    ""
    ""
)

echo "--- ЗАПУСК ТЕСТОВ ДЕШИФРАТОРА ---"

for i in "${!inputs[@]}"; do
    # Запускаем скрипт, передавая строку через echo -n (без лишнего переноса)
    result=$(echo -n "${inputs[$i]}" | python3 decrypt.py)
    
    if [ "$result" == "${expected[$i]}" ]; then
        echo "✅ Тест $((i+1)) ПРОЙДЕН: [${inputs[$i]}] -> [$result]"
    else
        echo "❌ Тест $((i+1)) ОШИБКА!"
        echo "   Вход:     ${inputs[$i]}"
        echo "   Ожидали:  ${expected[$i]}"
        echo "   Получили: $result"
    fi
done

echo "--- ТЕСТИРОВАНИЕ ЗАВЕРШЕНО ---"
